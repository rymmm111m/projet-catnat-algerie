export function getWilayaZone(wilayaName: string): {
  zone: string;
  color: string;
  label: string;
} {
  const name = wilayaName.toUpperCase();
  if (ZONE_CLASSIFICATIONS.ZONE_III.wilayas.includes(name)) {
    return {
      zone: "III",
      color: ZONE_CLASSIFICATIONS.ZONE_III.color,
      label: ZONE_CLASSIFICATIONS.ZONE_III.label,
    };
  }
  if (ZONE_CLASSIFICATIONS.ZONE_II.wilayas.includes(name)) {
    return {
      zone: "II",
      color: ZONE_CLASSIFICATIONS.ZONE_II.color,
      label: ZONE_CLASSIFICATIONS.ZONE_II.label,
    };
  }
  return {
    zone: "I",
    color: ZONE_CLASSIFICATIONS.ZONE_I.color,
    label: ZONE_CLASSIFICATIONS.ZONE_I.label,
  };
}

