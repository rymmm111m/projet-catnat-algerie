// Risk calculation functions
export function calculateRiskScore(
  zoneRPA: string,
  propertyType: string,
  capital: number
): { score: number; premium: number } {
  const baseRates: Record<string, number> = {
    "0": 0.0001,
    I: 0.0003,
    IIa: 0.0007,
    IIb: 0.0012,
    III: 0.0020,
    IV: 0.0030,
  };

  const typeMultipliers: Record<string, number> = {
    "Immobilier (Habitation)": 1.0,
    "Commercial (Bureaux/Magasins)": 1.4,
    "Industriel (Usines/Depots)": 2.2,
  };

  const zoneScores: Record<string, number> = {
    "0": 10,
    I: 25,
    IIa: 45,
    IIb: 65,
    III: 85,
    IV: 100,
  };

  const typeScores: Record<string, number> = {
    "Immobilier (Habitation)": 20,
    "Commercial (Bureaux/Magasins)": 50,
    "Industriel (Usines/Depots)": 90,
  };

  const baseRate = baseRates[zoneRPA] || 0.001;
  const typeMult = typeMultipliers[propertyType] || 1.0;

  const zoneScore = zoneScores[zoneRPA] || 50;
  const typeScore = typeScores[propertyType] || 50;

  let finalScore = zoneScore * 0.6 + typeScore * 0.4;
  if (capital > 1_000_000_000) finalScore += 10;

  return {
    score: Math.min(finalScore, 100),
    premium: capital * baseRate * typeMult,
  };
}

// Monte Carlo simulation
export function runMonteCarloSimulation(
  exposure: number,
  frequency: number,
  numSimulations: number = 100000
): {
  losses: number[];
  meanLoss: number;
  var995: number;
  maxLoss: number;
} {
  const sigma = 0.8;
  const meanMDR = 0.05;
  const losses: number[] = [];

  for (let i = 0; i < numSimulations; i++) {
    // Poisson distribution for number of events
    const numEvents = poissonRandom(frequency);
    let totalLoss = 0;

    for (let j = 0; j < numEvents; j++) {
      // Log-normal distribution for severity
      const muAdj = Math.log(meanMDR) - 0.5 * sigma * sigma;
      const mdr = Math.min(lognormalRandom(muAdj, sigma), 0.8);
      totalLoss += exposure * mdr;
    }

    losses.push(totalLoss);
  }

  losses.sort((a, b) => a - b);

  const meanLoss = losses.reduce((a, b) => a + b, 0) / losses.length;
  const var995Index = Math.floor(losses.length * 0.995);
  const var995 = losses[var995Index] || 0;
  const maxLoss = losses[losses.length - 1] || 0;

  return { losses, meanLoss, var995, maxLoss };
}

function poissonRandom(lambda: number): number {
  const L = Math.exp(-lambda);
  let k = 0;
  let p = 1;

  do {
    k++;
    p *= Math.random();
  } while (p > L);

  return k - 1;
}

function lognormalRandom(mu: number, sigma: number): number {
  const u1 = Math.random();
  const u2 = Math.random();
  const z = Math.sqrt(-2 * Math.log(u1)) * Math.cos(2 * Math.PI * u2);
  return Math.exp(mu + sigma * z);
}
